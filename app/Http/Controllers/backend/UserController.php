    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $user = User::findOrFail($id);
        $roles = Role::all();
        return view('backend.users.update',compact('user','roles'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::findOrFail($id);
        $user->name=$request->name;
        $user->email=$request->email;
        $user->save();
        $user->syncRoles($request->role);
        flash()->addSuccess('Kullancı Başarıyla Güncellendi');
        return  redirect()->route('kullanici.index');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::findOrFail($id);
        $user->delete();
        flash()->addSuccess('Kullancı başarıyla silindi');
        return redirect()->route('kullanici.index');
    }


    public function status(Request $request)
    {

        $user= User::findOrFail($request->id);
        $user->status=$request->statu =="true" ? 1 : 0;
        $user->save();
        flash()->addSuccess('Kullancıı  durumu güncellendi');
        return  redirect()->route('kullanici.index');
    }

}
